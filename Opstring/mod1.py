def maxLen(Frase1,Frase2):
    if len(Frase1)>len(Frase2):
        print(f"A primeira String é maior com: {len(Frase1)}")
    else:
        print(f"A segunda String é maior com:{len(Frase2)} ")

def minLen(Frase1,Frase2):
    if len(Frase1)<len(Frase2):
        print(f"A primeira String é menor com: {len(Frase1)}")
    else:
        print(f"A segunda String é menor com: {len(Frase2)}")